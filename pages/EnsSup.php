            <li class="ens1" id="4">
              <h3>Modalités d'inscription :</h3>
            </li>
          </ol>
          <ol>
            <li class="ens2" id="4-a">
              <h4>Etablissements publics :</h4>
              <p>Il faut savoir que nous pouvons accéder à un établissement en fonction du cycle souhaité : premier cycle (Licence),
                deuxième cycle(Master) et Troisième cycle (Doctorat).</p>
            </li>
          </ol>
          <ol>
            <li class="ens3">
              <h5>Licence</h5>
              <p>Pour les étrangers l’accès à la première année universitaire est facilité par l’Agence Marocaine de Coopération Internationale.
                En ce sens, il suffit de déposer les dossiers auprès de l’AMCI et après un avis favorable de leur part, une autorisation est délivrée à l’étudiant
                afin de s’inscrire dans un établissement.</p>
              <p>En revanche, nous pouvons signaler que durant le cycle, un changement d’établissement et même de filière est possible.</p>
            </li>
            <li class="ens3">
              <h5>Master</h5>
              <p>Actuellement pour accéder à ce cycle, il faut passer par les étapes suivants :</p>
              <ul>
                <li><b>Première étape </b>: Dépôt de dossiers (fournir les pièces demandées)</li>
                <li><b>Deuxième étape </b>: Concours écrit</li>
                <li><b>Troisième étape </b>: Entretien oral</li>
              </ul>
              <p>Après ces étapes, si on est retenu, il reste juste l’inscription définitive afin de débuter les cours.</p>
            </li>
            <li class="ens3">
              <h5>Doctorat</h5>
              <p>Pour accéder à ce cycle, il suffit tout d’obtenir son diplôme de Master (ou équivalent) et après suivre les étapes suivantes :</p>
              <ul>
                <li><b>Première étape : </b>Dépôt de dossiers (fournir les pièces demandées)</li>
                <li><b>Deuxième étape : </b>Entretien oral</li>
              </ul>
              <p>Une fois qu’on est retenu, il reste juste l’inscription définitive.</p></br>
              <p><b>Remarque : </b>
                Il ne faut pas oublier qu’une fois retenue pour le cycle Master ou doctoral,
                nous devons passer à l’AMCI pour faire une demande d’autorisation avec les pièces ci-dessous en fonction du cycle concerné :
              </p>
              <ul>
                <li><b>Cycle Master :</b>
                  <p>Le diplôme de Licence + une attestation de pré-inscription ou bien une lettre d’accord (montrant qu’on est accepté pour poursuivre les études)
                    + lettre adressée à l’Ambassadeur Directeur Général de l’AMCI ;</p>
                </li>
                <li><b>Cycle Doctorat :</b>
                  <p>Le diplôme de Master + une attestation de pré-inscription ou bien une lettre d’accord (montrant qu’on est accepté pour poursuivre les études)
                    + lettre adressée à l’Ambassadeur Directeur Général de l’AMCI ;</p>
                </li>
              </ul>
            </li>
          </ol>
          <ol start="2">
            <li class="ens2" id="4-b">
              <h4>Etablissements privés :</h4>
              <p>L’enseignement supérieur marocain est scindé en deux catégories. D’un côté, l’enseignement supérieur est organisé par
                le Ministère de l’enseignement supérieur. Il forme des cadres de niveau bac + 3 à bac + 8.
                De l’autre, le Ministère de l’emploi et de la formation professionnelle propose un enseignement professionnel visant à former des techniciens spécialisés
                en 2 ou 3 ans après le bac.</P></br>
              <p>Il existe des établissements privés dans les deux catégories d’enseignements. L’enseignement supérieur comprend des universités,
                des écoles et des Instituts technologiques.</p></br>
              <p>Grâce à une récente réforme, les études sont organisées sur le même modèle européen LMD : Licence en 3 ans, Master en 5 ans et Doctorat en 8 ans.</p>
            </li>
          </ol>
